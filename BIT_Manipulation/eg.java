package BIT_Manipulation;

public class eg {
    public static void main(String[] args) {
        System.out.println(5 & 6);
        System.out.println(5 | 6);
        System.out.println(5^6);
        System.out.println(~5);
        System.out.println(5 << 6); // a<<b , then  a * 2^b
        System.out.println(5 >> 6); // a>>b , then a/2^b
    }
    
}
